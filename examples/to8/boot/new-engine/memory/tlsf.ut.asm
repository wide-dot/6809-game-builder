tlsf.ut.mappingsearch
        ldx   #tlsf.ut.mappingsearch.in
        ldy   #tlsf.ut.mappingsearch.out
!       ldd   ,x++
        pshs  x,y
        jsr   tlsf.mappingsearch
        puls  x,y
        lda   tlsf.fl
        ldb   tlsf.sl
        cmpd  ,y++
        bne   *
        cmpx  #tlsf.ut.mappingsearch.in.end
        bne   <
        rts

